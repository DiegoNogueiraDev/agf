const test = require('node:test')
const assert = require('node:assert')
const path = require('node:path')
const fs = require('node:fs')

test('graph-state.json was generated by pipeline', () => {
  const statePath = path.join(__dirname, 'graph-state.json')
  assert.ok(fs.existsSync(statePath), 'graph-state.json must exist after pipeline run')
  const state = JSON.parse(fs.readFileSync(statePath, 'utf8'))
  assert.ok(state.nodes, 'graph-state must have nodes array')
  assert.ok(Array.isArray(state.nodes), 'nodes must be an array')
  assert.ok(state.nodes.length > 0, 'graph must have at least 1 node')
})

test('add_node: graph has at least 2 nodes', () => {
  const statePath = path.join(__dirname, 'graph-state.json')
  const state = JSON.parse(fs.readFileSync(statePath, 'utf8'))
  assert.ok(state.nodes.length >= 2, 'agent must have added at least 2 nodes via add_node')
})

test('edge: graph has an edge connecting nodes', () => {
  const statePath = path.join(__dirname, 'graph-state.json')
  const state = JSON.parse(fs.readFileSync(statePath, 'utf8'))
  assert.ok(state.edges, 'graph-state must have edges array')
  assert.ok(Array.isArray(state.edges), 'edges must be an array')
  assert.ok(state.edges.length > 0, 'agent must have created at least 1 edge')
})

test('update_status: at least one node reached status done', () => {
  const statePath = path.join(__dirname, 'graph-state.json')
  const state = JSON.parse(fs.readFileSync(statePath, 'utf8'))
  const doneNodes = state.nodes.filter((n) => n.status === 'done')
  assert.ok(doneNodes.length > 0, 'agent must have called update_status(done) on at least 1 node')
})

test('analyze: nodes have descriptions showing analysis', () => {
  const statePath = path.join(__dirname, 'graph-state.json')
  const state = JSON.parse(fs.readFileSync(statePath, 'utf8'))
  const withDesc = state.nodes.filter((n) => n.description && n.description.length > 5)
  assert.ok(withDesc.length > 0, 'agent must have called analyze on at least 1 node (description > 5 chars)')
})
